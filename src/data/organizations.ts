/** Returns organizations visible for public launch: high/medium confidence, status Active/Checked/Opportunity Open. */
export function getOrganizationsForLaunch(): Organization[] {
  return organizations.filter(
    (org) =>
      (LAUNCH_VISIBLE_CONFIDENCE as readonly string[]).includes(org.confidence) &&
      (LAUNCH_VISIBLE_STATUSES as readonly string[]).includes(org.statusLabel)
  );
}

/** Returns first 2 active signals for card display. */
export function getCardSignals(org: Organization): string[] {
  return org.activeSignals.slice(0, 2);
}
